#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(char))
{
	char	*str;
	size_t	i;
	
	if(!(str = ft_strdup(s)))
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	return (str);	
}

